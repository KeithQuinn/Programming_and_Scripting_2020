result = float (input("Enter student result: "))

if result < 0 or result > 100:
    print("Error")

elif result < 40:
    print("Fail")

elif result < 50:
    print("Pass")

elif result < 60:
    print("Merit 2")

elif result < 70:
    print("Merit 1")

else:
    print("Distinction")

    




    
